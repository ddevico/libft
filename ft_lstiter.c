#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	if (lst != 0 && f != 0)
	{
		while (lst != 0)
		{
			(*f)(lst);
			lst = lst->next;
		}
	}
}
